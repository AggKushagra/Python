a=int(input("Enter the first number"))
b=int(input("Enter the second number"))
c=input("Enter the operator")

if c=='+':
    print("The answer is",a+b)
elif c=='-':
    print("The answer is", a-b)
elif c=='*':
    print("The answer is", a*b)
elif c == '/':
    print("The answer is", a/b)
else:
    print("Wrong Operator")







